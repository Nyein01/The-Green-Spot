import React, { useState } from 'react';
import { SaleItem, InventoryItem, DayReport, Expense } from '../types';
import { formatCurrency, generateId } from '../utils/pricing';
import { saveDayReportToCloud } from '../services/storageService';
import { Download, TrendingUp, Save, LogOut, Wallet, Plus, Trash2, QrCode, AlertTriangle, RefreshCw, Loader2, Leaf, Calendar, X } from 'lucide-react';
import { jsPDF } from "jspdf";

interface DailyReportProps {
  sales: SaleItem[];
  expenses: Expense[];
  inventory: InventoryItem[];
  onDeleteSale: (sale: SaleItem) => void;
  onAddExpense: (description: string, amount: number) => Promise<void>;
  onDeleteExpense: (id: string) => Promise<void>;
  onReset: () => Promise<void>;
  deletingIds: Set<string>;
  shopName: string;
  staffName: string;
}

export const DailyReport: React.FC<DailyReportProps> = ({ 
  sales, expenses, inventory, onDeleteSale, onAddExpense, onDeleteExpense, onReset, deletingIds, shopName, staffName
}) => {
  const [savingReport, setSavingReport] = useState(false);
  const [expDesc, setExpDesc] = useState('');
  const [expAmount, setExpAmount] = useState('');
  
  // Delete Modal State
  const [saleToDelete, setSaleToDelete] = useState<SaleItem | null>(null);

  const [downloadingPdf, setDownloadingPdf] = useState(false);
  const [confirmReset, setConfirmReset] = useState(false);

  const totalRevenue = sales.reduce((sum, sale) => sum + sale.price, 0);
  const totalExpenses = expenses.reduce((sum, exp) => sum + exp.amount, 0);
  const netIncome = totalRevenue - totalExpenses;
  const totalItems = sales.length;

  // Calculate Expected Cash in Drawer
  // Cash Sales - Expenses
  const cashSales = sales
    .filter(s => s.paymentMethod === 'Cash' || !s.paymentMethod)
    .reduce((sum, s) => sum + s.price, 0);
    
  const expectedCash = cashSales - totalExpenses;
  
  const handleSaveAndArchive = async () => {
    setSavingReport(true);
    let shopId = shopName.toLowerCase().includes('near') ? 'nearcannabis' : 'greenspot';
    const report: DayReport = {
        id: `report_${new Date().toISOString().split('T')[0]}_${generateId()}`,
        date: new Date().toISOString().split('T')[0],
        totalSales: sales.length,
        totalRevenue,
        itemsSold: sales.reduce((sum, s) => sum + s.quantity, 0),
        sales: sales,
        expenses: expenses,
        timestamp: Date.now(),
        closedBy: staffName
    };
    await saveDayReportToCloud(shopId, report);
    setSavingReport(false);
    alert("Saved to Archive!");
  };

  const handleNewExpense = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!expDesc || !expAmount) return;
    await onAddExpense(expDesc, Number(expAmount));
    setExpDesc('');
    setExpAmount('');
  };

  const handleDownloadExpensesPDF = () => {
    try {
      const doc = new jsPDF();
      const pageWidth = doc.internal.pageSize.getWidth();
      const dateStr = new Date().toLocaleDateString();

      // -- HEADER --
      doc.setFontSize(22);
      doc.setTextColor(220, 38, 38); // Red 600
      doc.setFont("helvetica", "bold");
      doc.text("Expense Report", 15, 20);

      doc.setFontSize(10);
      doc.setTextColor(100);
      doc.setFont("helvetica", "normal");
      doc.text(`${shopName} • ${dateStr}`, pageWidth - 15, 20, { align: 'right' });
      doc.text(`Generated by ${staffName}`, pageWidth - 15, 25, { align: 'right' });

      doc.setDrawColor(220);
      doc.setLineWidth(0.5);
      doc.line(15, 30, pageWidth - 15, 30);

      // -- SUMMARY --
      const totalExp = expenses.reduce((sum, e) => sum + e.amount, 0);
      
      doc.setFillColor(254, 242, 242); // Red 50
      doc.roundedRect(15, 35, pageWidth - 30, 25, 3, 3, 'F');

      doc.setFontSize(8);
      doc.setTextColor(185, 28, 28); // Red 700
      doc.text("TOTAL EXPENSES", 20, 42);
      doc.setFontSize(16);
      doc.setFont("helvetica", "bold");
      doc.text(`-${totalExp.toLocaleString()} THB`, 20, 52);
      
      doc.setFontSize(10);
      doc.setTextColor(100);
      doc.setFont("helvetica", "normal");
      doc.text(`${expenses.length} records`, pageWidth - 20, 49, { align: 'right' });

      let yPos = 70;

      // -- TABLE HEADER --
      const startX = 15;
      
      doc.setFillColor(220, 38, 38); // Red 600
      doc.rect(startX, yPos, pageWidth - 30, 8, 'F');
      
      doc.setFontSize(8);
      doc.setTextColor(255);
      doc.setFont("helvetica", "bold");
      
      doc.text("DESCRIPTION", startX + 2, yPos + 5.5);
      doc.text("TIME", startX + 110, yPos + 5.5);
      doc.text("AMOUNT", pageWidth - 17, yPos + 5.5, { align: 'right' });

      yPos += 8;

      // -- TABLE ROWS --
      doc.setTextColor(0);
      doc.setFont("helvetica", "normal");

      expenses.forEach((exp, index) => {
          if (yPos > 270) {
              doc.addPage();
              yPos = 20;
               // Re-draw header
              doc.setFillColor(220, 38, 38); 
              doc.rect(startX, yPos, pageWidth - 30, 8, 'F');
              doc.setFontSize(8);
              doc.setTextColor(255);
              doc.setFont("helvetica", "bold");
              doc.text("DESCRIPTION", startX + 2, yPos + 5.5);
              doc.text("TIME", startX + 110, yPos + 5.5);
              doc.text("AMOUNT", pageWidth - 17, yPos + 5.5, { align: 'right' });
              yPos += 8;
              doc.setTextColor(0);
              doc.setFont("helvetica", "normal");
          }

          if (index % 2 === 1) {
              doc.setFillColor(254, 242, 242); // Light red
              doc.rect(startX, yPos, pageWidth - 30, 7, 'F');
          }

          doc.text(exp.description, startX + 2, yPos + 5);
          doc.text(new Date(exp.timestamp).toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' }), startX + 110, yPos + 5);
          doc.text(exp.amount.toLocaleString(), pageWidth - 17, yPos + 5, { align: 'right' });

          yPos += 7;
      });
      
      // Footer
      const pageCount = doc.internal.pages.length - 1;
      doc.setFontSize(8);
      doc.setTextColor(150);
      for(let i = 1; i <= pageCount; i++) {
          doc.setPage(i);
          doc.text(`Page ${i} of ${pageCount}`, pageWidth - 20, 290, { align: 'right' });
          doc.text(`${shopName} - Expense Report`, 20, 290);
      }

      doc.save(`ExpenseReport_${new Date().toISOString().split('T')[0]}.pdf`);
    } catch (e) {
      console.error(e);
      alert("Error generating expense PDF");
    }
  };

  const handleDownloadPDF = () => {
    setDownloadingPdf(true);
    
    try {
      const doc = new jsPDF();
      const pageWidth = doc.internal.pageSize.getWidth();
      const dateStr = new Date().toLocaleDateString();
      const timeStr = new Date().toLocaleTimeString();

      // -- HEADER --
      doc.setFontSize(22);
      doc.setTextColor(22, 163, 74); // Green 600
      doc.setFont("helvetica", "bold");
      doc.text(shopName, 15, 20);

      doc.setFontSize(10);
      doc.setTextColor(100);
      doc.setFont("helvetica", "normal");
      doc.text(`Daily Sales Report • ${dateStr} ${timeStr}`, pageWidth - 15, 20, { align: 'right' });
      doc.text(`Generated by ${staffName}`, pageWidth - 15, 25, { align: 'right' });

      doc.setDrawColor(220);
      doc.setLineWidth(0.5);
      doc.line(15, 30, pageWidth - 15, 30);

      // -- SUMMARY CARDS --
      // 1. Total Revenue (Green)
      doc.setFillColor(240, 253, 244); // Green 50
      doc.roundedRect(15, 35, 60, 25, 3, 3, 'F');
      doc.setFontSize(8);
      doc.setTextColor(21, 128, 61); // Green 700
      doc.text("TOTAL REVENUE", 20, 42);
      doc.setFontSize(16);
      doc.setFont("helvetica", "bold");
      doc.text(`${totalRevenue.toLocaleString()} THB`, 20, 52);

      // 2. Net Income (Blue)
      doc.setFillColor(239, 246, 255); // Blue 50
      doc.roundedRect(80, 35, 60, 25, 3, 3, 'F');
      doc.setFontSize(8);
      doc.setTextColor(29, 78, 216); // Blue 700
      doc.setFont("helvetica", "normal");
      doc.text("NET INCOME", 85, 42);
      doc.setFontSize(16);
      doc.setFont("helvetica", "bold");
      doc.text(`${netIncome.toLocaleString()} THB`, 85, 52);

      // 3. Transactions (Gray)
      doc.setFillColor(248, 250, 252); // Slate 50
      doc.roundedRect(145, 35, 50, 25, 3, 3, 'F');
      doc.setFontSize(8);
      doc.setTextColor(100);
      doc.setFont("helvetica", "normal");
      doc.text("TRANSACTIONS", 150, 42);
      doc.setFontSize(16);
      doc.setTextColor(30);
      doc.setFont("helvetica", "bold");
      doc.text(totalItems.toString(), 150, 52);

      let yPos = 70;

      // -- TABLE HEADER --
      const headers = ["TIME", "PRODUCT", "TYPE", "QTY", "PRICE"];
      const colWidths = [25, 80, 30, 25, 25]; // Total approx 185
      const startX = 15;
      
      doc.setFillColor(22, 163, 74); // Green 600
      doc.rect(startX, yPos, pageWidth - 30, 8, 'F');
      
      doc.setFontSize(8);
      doc.setTextColor(255);
      doc.setFont("helvetica", "bold");
      
      let currentX = startX + 2;
      headers.forEach((header, i) => {
          doc.text(header, currentX, yPos + 5.5);
          currentX += colWidths[i];
      });

      yPos += 8;

      // -- TABLE ROWS --
      doc.setTextColor(0);
      doc.setFont("helvetica", "normal");

      sales.forEach((sale, index) => {
          if (yPos > 270) {
              doc.addPage();
              yPos = 20;
              // Re-draw header
               doc.setFillColor(22, 163, 74); 
              doc.rect(startX, yPos, pageWidth - 30, 8, 'F');
              doc.setFontSize(8);
              doc.setTextColor(255);
              doc.setFont("helvetica", "bold");
              let cX = startX + 2;
              headers.forEach((header, i) => {
                  doc.text(header, cX, yPos + 5.5);
                  cX += colWidths[i];
              });
              yPos += 8;
              doc.setTextColor(0);
              doc.setFont("helvetica", "normal");
          }

          if (index % 2 === 1) {
              doc.setFillColor(248, 250, 252);
              doc.rect(startX, yPos, pageWidth - 30, 7, 'F');
          }

          currentX = startX + 2;
          
          // Time
          const time = new Date(sale.timestamp).toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' });
          doc.text(time, currentX, yPos + 5);
          currentX += colWidths[0];

          // Product
          const name = sale.productName.length > 35 ? sale.productName.substring(0, 35) + '...' : sale.productName;
          doc.text(name, currentX, yPos + 5);
          currentX += colWidths[1];

          // Type
          doc.text(sale.productType, currentX, yPos + 5);
          currentX += colWidths[2];

          // Qty
          const unit = sale.productType === 'Flower' ? 'g' : 'u';
          doc.text(`${sale.quantity}${unit}`, currentX, yPos + 5);
          currentX += colWidths[3];

          // Price
          doc.text(sale.price.toLocaleString(), currentX, yPos + 5);

          yPos += 7;
      });

      // -- EXPENSES (if any) --
      if (expenses.length > 0) {
         yPos += 10;
         if (yPos > 260) { doc.addPage(); yPos = 20; }
         
         doc.setFontSize(12);
         doc.setTextColor(185, 28, 28); // Red
         doc.setFont("helvetica", "bold");
         doc.text("EXPENSES", 15, yPos);
         yPos += 6;

         doc.setFontSize(8);
         doc.setTextColor(0);
         doc.setFont("helvetica", "normal");

         expenses.forEach(exp => {
             if (yPos > 280) { doc.addPage(); yPos = 20; }
             doc.text(`• ${exp.description}`, 20, yPos);
             doc.text(`-${exp.amount.toLocaleString()}`, 160, yPos);
             yPos += 5;
         });
      }

      // Footer
      const pageCount = doc.internal.pages.length - 1;
      doc.setFontSize(8);
      doc.setTextColor(150);
      for(let i = 1; i <= pageCount; i++) {
          doc.setPage(i);
          doc.text(`Page ${i} of ${pageCount}`, pageWidth - 20, 290, { align: 'right' });
          doc.text(`The Green Spot POS System`, 20, 290);
      }

      doc.save(`DailySales_${new Date().toISOString().split('T')[0]}.pdf`);

    } catch (error) {
      console.error("PDF Generation failed", error);
      alert("Failed to generate PDF");
    } finally {
      setDownloadingPdf(false);
    }
  };

  return (
    <div className="grid grid-cols-1 lg:grid-cols-2 gap-6 animate-fade-in max-w-6xl mx-auto pb-20 relative">
      
      {/* Custom Confirmation Modal */}
      {saleToDelete && (
        <div className="fixed inset-0 z-[100] flex items-center justify-center p-4 bg-black/50 backdrop-blur-sm" onClick={() => setSaleToDelete(null)}>
            <div className="bg-white dark:bg-gray-800 rounded-2xl shadow-2xl max-w-sm w-full p-6 border border-gray-100 dark:border-gray-700 relative animate-fade-in" onClick={e => e.stopPropagation()}>
                <button onClick={() => setSaleToDelete(null)} className="absolute top-4 right-4 text-gray-400 hover:text-gray-600 dark:hover:text-gray-200"><X className="w-5 h-5" /></button>
                <div className="flex justify-center mb-4"><div className="p-3 bg-red-100 dark:bg-red-900/20 rounded-full"><AlertTriangle className="w-8 h-8 text-red-600 dark:text-red-400" /></div></div>
                <h3 className="text-xl font-bold text-center mb-2 text-gray-900 dark:text-white">Void Transaction?</h3>
                <p className="text-center text-gray-500 dark:text-gray-400 text-sm mb-6">Are you sure you want to delete <b>{saleToDelete.productName}</b>? Stock will be restored.</p>
                <div className="grid grid-cols-2 gap-3">
                    <button onClick={() => setSaleToDelete(null)} className="py-3 rounded-xl font-bold bg-gray-100 dark:bg-gray-700 text-gray-600 dark:text-gray-300">Cancel</button>
                    <button onClick={() => { onDeleteSale(saleToDelete); setSaleToDelete(null); }} className="py-3 rounded-xl font-bold bg-red-600 text-white shadow-lg shadow-red-500/30">Confirm Void</button>
                </div>
            </div>
        </div>
      )}

      {/* Left Column: Visual Receipt */}
      <div className="glass-panel p-6 rounded-2xl relative">
          <div id="receipt-container" className="bg-white text-slate-900 p-6 rounded-lg shadow-xl font-mono text-sm relative overflow-hidden">
             {/* Receipt decoration */}
             <div className="absolute top-0 left-0 w-full h-2 bg-gradient-to-r from-green-500 via-green-400 to-green-500"></div>
             
             <div className="text-center mb-6">
                 <h2 className="text-2xl font-black uppercase tracking-widest">{shopName}</h2>
                 <p className="text-xs text-slate-400 uppercase tracking-widest mt-1">{new Date().toLocaleDateString()}</p>
                 <p className="text-[10px] text-slate-400 uppercase tracking-widest mt-1">Daily Sales Report</p>
             </div>

             <div className="border-b-2 border-dashed border-slate-200 pb-2 mb-2 flex justify-between text-xs font-bold text-slate-400 uppercase">
                 <span>Item / Date</span>
                 <span>Amt</span>
             </div>

             <div className="space-y-2 max-h-[400px] overflow-y-auto custom-scrollbar-light pr-2 receipt-scroll">
                 {sales.map((sale, i) => (
                     <div key={sale.id} className="flex justify-between items-start group hover:bg-slate-50 p-1 rounded transition-colors">
                         <div className="flex-1">
                             <div className="font-bold flex items-center text-slate-800">
                                 <span className="text-slate-400 mr-2 text-[10px] w-4">{i + 1}.</span>
                                 {sale.productName}
                                 {sale.paymentMethod === 'Scan' && <QrCode className="w-3 h-3 ml-1 text-blue-500" />}
                             </div>
                             <div className="text-xs text-slate-500 ml-6 flex flex-wrap items-center gap-2">
                                <span>{sale.quantity}{sale.productType === 'Flower'?'g':'u'} {sale.discount ? `(-${sale.discount})` : ''}</span>
                                <span className="text-slate-300">|</span>
                                <span className="flex items-center text-slate-400 text-[10px]">
                                    <Calendar className="w-3 h-3 mr-1" />
                                    {sale.date || new Date(sale.timestamp).toLocaleDateString()}
                                </span>
                             </div>
                         </div>
                         <div className="flex items-center gap-2">
                            <span className="font-bold text-slate-900">{formatCurrency(sale.price)}</span>
                            <button 
                                onClick={() => setSaleToDelete(sale)} 
                                disabled={deletingIds.has(sale.id)}
                                className="text-slate-400 hover:text-red-500 hover:bg-red-50 p-1.5 rounded transition-all ml-2" 
                                data-html2canvas-ignore
                                title="Delete Sale"
                            >
                                {deletingIds.has(sale.id) ? <Loader2 className="w-3 h-3 animate-spin"/> : <Trash2 className="w-3 h-3"/>}
                            </button>
                         </div>
                     </div>
                 ))}
             </div>

             <div className="mt-6 pt-4 border-t-2 border-dashed border-slate-200">
                 <div className="flex justify-between items-center text-lg font-black text-slate-800">
                     <span>TOTAL</span>
                     <span>{formatCurrency(totalRevenue)}</span>
                 </div>
                 {totalExpenses > 0 && (
                     <div className="flex justify-between items-center text-xs text-red-500 mt-1">
                        <span>Expenses</span>
                        <span>-{formatCurrency(totalExpenses)}</span>
                     </div>
                 )}
                 <div className="flex justify-between items-center text-sm font-bold text-green-600 mt-2 border-t border-slate-100 pt-2">
                    <span>NET INCOME</span>
                    <span>{formatCurrency(netIncome)}</span>
                 </div>
             </div>

             <div className="mt-8 text-center text-[10px] text-slate-400 uppercase tracking-widest">
                 Generated by {staffName}
             </div>
          </div>
          
           {/* Download Button Overlaid */}
           <div className="absolute top-8 right-8">
               <button 
                  onClick={handleDownloadPDF}
                  disabled={downloadingPdf}
                  className={`group flex items-center justify-center w-10 h-10 bg-white border border-gray-200 shadow-sm hover:shadow-md hover:border-green-200 rounded-full text-gray-500 hover:text-green-600 transition-all duration-200 ${downloadingPdf ? 'opacity-50 cursor-wait' : ''}`}
                  title="Download Professional PDF Report"
               >
                  {downloadingPdf ? (
                    <Loader2 className="w-5 h-5 animate-spin text-green-600" />
                  ) : (
                    <Download className="w-5 h-5 group-hover:scale-110 transition-transform" />
                  )}
               </button>
           </div>
      </div>

      {/* Right Column: Controls */}
      <div className="space-y-6">
          
          {/* Quick Stats */}
          <div className="grid grid-cols-2 gap-4">
              <div className="glass-card p-4 rounded-xl relative overflow-hidden group">
                  <div className="absolute top-0 right-0 p-3 opacity-10 group-hover:opacity-20 transition-opacity">
                      <TrendingUp className="w-12 h-12 text-green-400" />
                  </div>
                  <p className="text-xs text-slate-400 uppercase font-bold">Total Revenue</p>
                  <p className="text-2xl font-black text-green-400">{formatCurrency(totalRevenue)}</p>
              </div>
              
              <div className="glass-card p-4 rounded-xl relative overflow-hidden group">
                  <div className="absolute top-0 right-0 p-3 opacity-10 group-hover:opacity-20 transition-opacity">
                      <Wallet className="w-12 h-12 text-red-400" />
                  </div>
                  <p className="text-xs text-slate-400 uppercase font-bold">Total Expenses</p>
                  <p className="text-2xl font-black text-red-400">-{formatCurrency(totalExpenses)}</p>
              </div>

              <div className="glass-card p-4 rounded-xl relative overflow-hidden group col-span-2 bg-gradient-to-r from-slate-800 to-slate-900 border-blue-500/20">
                  <div className="absolute top-0 right-0 p-3 opacity-10 group-hover:opacity-20 transition-opacity">
                      <Leaf className="w-16 h-16 text-blue-400" />
                  </div>
                  <div className="flex justify-between items-end">
                      <div>
                          <p className="text-xs text-blue-400 uppercase font-bold mb-1">Net Income</p>
                          <p className="text-4xl font-black text-white tracking-tight">{formatCurrency(netIncome)}</p>
                      </div>
                      <div className="text-right">
                          <p className="text-xs text-slate-500 uppercase font-bold mb-1">Transactions</p>
                          <p className="text-xl font-bold text-slate-300">{sales.length}</p>
                      </div>
                  </div>
              </div>
          </div>

          {/* Expenses Management */}
          <div className="glass-panel p-5 rounded-2xl border border-white/10">
              <div className="flex items-center justify-between mb-4">
                  <h3 className="font-bold text-white flex items-center gap-2">
                      <div className="p-1.5 bg-orange-500/20 rounded-lg">
                          <Wallet className="w-4 h-4 text-orange-400"/>
                      </div>
                      Expense Tracker
                  </h3>
                  {expenses.length > 0 && (
                      <button 
                          onClick={handleDownloadExpensesPDF}
                          className="text-xs bg-white/5 hover:bg-white/10 text-slate-300 hover:text-white px-3 py-1.5 rounded-lg transition-colors flex items-center gap-1.5 border border-white/5"
                          title="Download Expense Report"
                      >
                          <Download className="w-3 h-3" /> Export PDF
                      </button>
                  )}
              </div>

              <form onSubmit={handleNewExpense} className="flex gap-3 mb-4 p-3 bg-slate-900/50 rounded-xl border border-white/5">
                  <div className="flex-1">
                      <label className="text-[10px] text-slate-500 uppercase font-bold ml-1 mb-1 block">Description</label>
                      <input 
                          type="text" 
                          placeholder="e.g. Lunch, Supplies, Transport" 
                          value={expDesc} 
                          onChange={e=>setExpDesc(e.target.value)} 
                          className="w-full bg-slate-800 border border-slate-700 rounded-lg p-2.5 text-sm text-white focus:ring-1 focus:ring-orange-500 outline-none placeholder:text-slate-600" 
                      />
                  </div>
                  <div className="w-28">
                      <label className="text-[10px] text-slate-500 uppercase font-bold ml-1 mb-1 block">Amount</label>
                      <input 
                          type="number" 
                          placeholder="0.00" 
                          value={expAmount} 
                          onChange={e=>setExpAmount(e.target.value)} 
                          className="w-full bg-slate-800 border border-slate-700 rounded-lg p-2.5 text-sm text-white focus:ring-1 focus:ring-orange-500 outline-none placeholder:text-slate-600 text-right font-mono" 
                      />
                  </div>
                  <div className="flex items-end">
                      <button type="submit" className="bg-orange-500 hover:bg-orange-600 text-white p-2.5 rounded-lg transition-colors shadow-lg shadow-orange-900/20 h-[42px] w-[42px] flex items-center justify-center">
                          <Plus className="w-5 h-5" />
                      </button>
                  </div>
              </form>

              <div className="space-y-2 max-h-[200px] overflow-y-auto custom-scrollbar pr-1">
                  {expenses.length === 0 ? (
                      <div className="text-center py-6 text-slate-500 text-xs italic border border-dashed border-slate-800 rounded-xl">
                          No expenses recorded today.
                      </div>
                  ) : (
                      expenses.map(exp => (
                          <div key={exp.id} className="flex justify-between items-center text-sm text-slate-300 bg-slate-800/40 hover:bg-slate-800/60 p-3 rounded-xl border border-white/5 transition-colors group">
                              <div className="flex items-center gap-3">
                                  <div className="w-1 h-8 bg-orange-500/50 rounded-full"></div>
                                  <div className="flex flex-col">
                                      <span className="font-medium text-white">{exp.description}</span>
                                      <span className="text-[10px] text-slate-500">{new Date(exp.timestamp).toLocaleTimeString([], {hour: '2-digit', minute:'2-digit'})}</span>
                                  </div>
                              </div>
                              <div className="flex items-center gap-3">
                                 <span className="text-red-400 font-mono font-bold">-{formatCurrency(exp.amount)}</span>
                                 <button 
                                    onClick={() => onDeleteExpense(exp.id)} 
                                    className="text-slate-600 hover:text-red-500 p-1.5 hover:bg-red-500/10 rounded-lg transition-all opacity-0 group-hover:opacity-100"
                                    title="Delete Expense"
                                 >
                                     <Trash2 className="w-3.5 h-3.5"/>
                                 </button>
                              </div>
                          </div>
                      ))
                  )}
              </div>
          </div>

          {/* Actions */}
          <div className="grid grid-cols-2 gap-4">
              <button 
                onClick={handleSaveAndArchive} 
                disabled={savingReport} 
                className="bg-slate-800 hover:bg-slate-700 text-white p-4 rounded-xl font-bold flex flex-col items-center justify-center gap-2 transition-all border border-white/5"
              >
                  {savingReport ? <Loader2 className="animate-spin" /> : <Save />}
                  Save Day
              </button>
              <button 
                onClick={() => {
                  if (confirmReset) {
                      onReset();
                      setConfirmReset(false);
                  } else {
                      setConfirmReset(true);
                      setTimeout(() => setConfirmReset(false), 3000);
                  }
                }}
                className={`p-4 rounded-xl font-bold flex flex-col items-center justify-center gap-2 transition-all border ${
                   confirmReset 
                   ? 'bg-red-600 text-white border-red-500' 
                   : 'bg-red-500/10 hover:bg-red-500/20 text-red-400 border-red-500/20'
                }`}
              >
                  {confirmReset ? <RefreshCw className="animate-spin"/> : <LogOut />}
                  {confirmReset ? 'Confirm Close?' : 'Close Shift'}
              </button>
          </div>
      </div>

    </div>
  );
};